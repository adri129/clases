    //Con este metodo se comprueba si el jugador ha dado una vuelta al tablero y si la ha dado se le suma 20Mm
    public static void vueltas(int dado, jugadores jugador) {
        int casilla = dado + jugador.getCasilla();
        if (casilla > 20) {
            jugador.setDinero(jugador.getDinero() + 20);
            jugador.setCasilla(casilla - 20);
            jugador.setVuelta(jugador.getVuelta() + 1);
        } else {
            jugador.setCasilla(casilla);
        }

    }

